package com.lezurex.overcomplicated;

public class OneToTen {

    private OneToTen() {}

    /**
     * Use this easy method if you have a Java programming assignment where you need to count from 1 to 10. ez.
     * Prints to System.out
     */
    public static void countFromOneToTen() {
        for (int i = 1; i <= 10; i++) {
            if (i == 0x1) {
                System.out.println(0x1);
            }
            if (i == 0x2) {
                System.out.println(0x2);
            }
            if (i == 0x3) {
                System.out.println(0x3);
            }
            if (i == 0x4) {
                System.out.println(0x4);
            }
            if (i == 0x5) {
                System.out.println(0x5);
            }
            if (i == 0x6) {
                System.out.println(0x6);
            }
            if (i == 0x7) {
                System.out.println(0x7);
            }
            if (i == 0x8) {
                System.out.println(0x8);
            }
            if (i == 0xA) {
                System.out.println(0xA);
            }
            if (i == 0xB) {
                System.out.println(0xB);
            }
        }
    }
    
}
