package com.lezurex.overcomplicated;

public class OneToTen {

    private OneToTen() {}

    /**
     * Use this easy method if you have a Java programming assignment where you need to count from 1 to 10. ez.
     * Prints to System.out
     */
    public static void countFromOneToTen() {
        for (int i = 0b1; i <= 0b1010; i++) {
            if (i == 0b1) {
                System.out.println(0b1);
            }
            if (i == 0b10) {
                System.out.println(0b10);
            }
            if (i == 0b11) {
                System.out.println(0b11);
            }
            if (i == 0b100) {
                System.out.println(0b100);
            }
            if (i == 0b101) {
                System.out.println(0b101);
            }
            if (i == 0b110) {
                System.out.println(0b110);
            }
            if (i == 0b111) {
                System.out.println(0b111);
            }
            if (i == 0b1000) {
                System.out.println(0b1000);
            }
            if (i == 0b1001) {
                System.out.println(0b1001);
            }
            if (i == 0b1010) {
                System.out.println(0b1010);
            }
        }
    }
    
}
